package week1.day2;

public class JumpStatementBreakStatement {

	public static void main(String[] args) {
		
		// break condition will break the loop 
		
		for (int i = 1; i <=10; i++) {
			
			
			if(i==7) {
				
				// by using break statment loop got stopped executing from 7 
				
				
				
				break;
			}
			
			System.out.println(i);
			
		}

	}

}
