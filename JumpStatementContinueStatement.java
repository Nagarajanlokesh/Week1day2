package week1.day2;

public class JumpStatementContinueStatement {

	public static void main(String[] args) {
		
		
		
		for (int i = 1; i<=5; i++) {
			
			//if i want to stop any itration then i should use continue statement 
			
		if(i==4) {
			
			continue;
			// So here 4 will be coming in output because in IF condition we stopped 4th itration 
			
			
			
		}
			
			System.out.println(i);
			
			
		}

	}

}
